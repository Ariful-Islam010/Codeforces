#include<stdio.h>
int main()
{
    int n,a[1000],b[1000],i,j,count=0;
    scanf("%d",&n);
    for(i=0;i<n;i++)
    {
        scanf("%d %d",&a[i],&b[i]);
    }
    for(i=0;i<n;i++)
    {
        for(j=0;j<n;j++)
          {
              if(a[i]==b[j]) count++;
          }
    }
    printf("%d\n",count);
    return 0;
}
